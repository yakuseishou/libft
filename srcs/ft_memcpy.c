#include <stdio.h>

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	size_t	i;
	char	*b;

	i = 0;
	b = (char *)s2;
	while (i < n)
		((char *)s1)[i++] = *b++;
	return (s1);
}
